package com.company.Lab3;

public class Book {
    private int Year;
    private String Name;

    public void setName(String name) {
        Name = name;
    }

    public void setYear(int year) {
        Year = year;
    }
    public int GetYear() {
        return Year;
    }
    public String GetName() {
        return Name;
    }
}
