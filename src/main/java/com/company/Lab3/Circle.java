package com.company.Lab3;

public class Circle {
    private int radius;
    private int length;
    private int square;

    public void setLength(int length) {
        this.length = length;
    }
    public void setRadius(int radius) {
        this.radius = radius;
    }
    public void setSquare(int square) {
        this.square = square;
    }
    public int getLength() {
        return length;
    }
    public int getRadius() {
        return radius;
    }
    public int getSquare() {
        return square;
    }
}
